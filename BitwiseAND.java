package com.thealgorithms.bitwiseop;

public class BitwiseAND {
    class Main {
        public static void main(String[] args) {

            int number1 = 45, number2 = 95, result;

            // bitwise XOR between 45 and 95
            result = number1 ^ number2;
            System.out.println(result);    // prints 114
        }
    }
}
