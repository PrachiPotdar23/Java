package com.thealgorithms.bitwiseop;

public class BitwiseRightShift {
    class Main {
        public static void main(String[] args) {

            int number1 = 4;
            int number2 = -4;


            System.out.println(number1 >> 2);    // prints 1
            System.out.println(number2 >> 2);    // prints -1
        }
    }
}
